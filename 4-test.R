varm <- var(momo)*19/20 #標本分散
heihoM <- varm*20 #平方和
bobun <- (heihoS+heihoM)/(17+19)#推定母分散

a <- sqrt(bobun*(1/18+1/20))#√推定母分散((1/サンプルサイズA)＋(1/サンプルサイズB))
t <- round(sa/a,2) #標本平均の差／a
print(t)#問3（t値）

