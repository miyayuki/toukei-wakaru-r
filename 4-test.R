#この標準誤差を求めるために、まず推定母分散を求める。推定母分散＝平均偏差の平方和／サンプルサイズ－1

#さくら組の平均偏差の平方和＝標本分散×サンプルサイズ18
vars <- var(sakura)*17/18 #標本分散
heihoS <- vars*18 #平方和
